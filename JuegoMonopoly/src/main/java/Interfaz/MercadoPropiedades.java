    private void cargarPropiedades(){
        for(Propiedad p:this.vendedor.getPatrimonio()){
            this.cmbPropiedades.addItem(p.getNombre());
        }
    }
    
    private void cargarJugadores(){
        
        for(Jugador j:this.jm.getJugadores()){
            if(!j.getNombre().equals(vendedor.getNombre())){
                this.cmbCompradores.addItem(j.getNombre());
            }
        }
    }

