    private void cmbPropiedadesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbPropiedadesActionPerformed

        if (!cmbPropiedades.getSelectedItem().toString().isEmpty()) {

            for (Propiedad p : this.vendedor.getPatrimonio()) {

                if (p.getNombre().equals(cmbPropiedades.getSelectedItem().toString())) {
                    this.txtDescripcion.setText(p.toString());
                }
            }
        }
    }//GEN-LAST:event_cmbPropiedadesActionPerformed

    private void VenderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_VenderActionPerformed

        Propiedad prop = null;
        for(Propiedad p:vendedor.getPatrimonio()){
            if(p.getNombre().equals(this.cmbPropiedades.getSelectedItem().toString())){
                prop = p;
            }
        }    
        this.jm.venderPropiedad(vendedor, this.cmbCompradores.getSelectedItem().toString(), prop, Integer.parseInt(this.txtValor.getText()));
        JOptionPane.showMessageDialog(null, "La Propiedad ha sido vendida");

    }//GEN-LAST:event_VenderActionPerformed

